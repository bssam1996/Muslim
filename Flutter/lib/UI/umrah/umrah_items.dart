# دليل مناسك العمرة

## المقدمة

هذه نبذة مختصرة عن أعمال مناسك العمرة، مستفادة من توجيهات الشيخ عبد العزيز بن باز رحمه الله والهدي النبوي الصحيح.

---

## 1. الاستعداد للإحرام

## النظافة وسنن الفطرة

* يُستحب قبل الإحرام: تقليم الأظافر، قص الشارب، نتف الإبط، وحلق العانة.
* **الاغتسال:** يُستحب الاغتسال والتنظف للرجل والمرأة (حتى الحائض والنفساء، لكنهما لا تطوفان حتى تطهرا).
* **التطيب:** يتطيب الرجل في بدنه (رأسه ولحيته) قبل لبس الإحرام، ولا يطيب ملابس الإحرام.

## لبس ملابس الإحرام

* **للرجل:** يتجرد من المخيط ويلبس إزارًا ورداءً (يُفضل أن يكونا أبيضين نظيفين).
* **للمرأة:** تحرم في ملابسها العادية الساترة التي لا زينة فيها، وتتجنب لبس النقاب والقفازين، وتغطي وجهها وكفيها عن الرجال الأجانب بغيرهما.

## الميقات (تنبيه للمسافرين)

* يجب عقد نية الإحرام عند الوصول إلى الميقات.
* **المسافر جواً أو بحراً:** يجب عليه الإحرام عند **محاذاة الميقات** في طريقه، ولا يجوز له تأخير الإحرام حتى الهبوط في المطار.

---

## 2. النية والتلبية ومحظورات الإحرام

## النية والاشتراط

* ينوي الدخول في النسك بقلبه ويقول بلسانه: "لبيك عمرة".
* **الاشتراط:** إذا خاف المحرم عائقاً (مرض أو عدو)، يقول: "فإن حبسني حابس فمحلي حيث حبستني"؛ ليكون له التحلل دون فدية إن تعسر عليه الإكمال.

## محظورات الإحرام (بمجرد الدخول في النية)

* يمتنع المحرم عن: قص الشعر والأظافر، التطيب، لبس المخيط وتغطية الرأس (للرجال)، لبس النقاب والقفازين (للنساء)، الجماع، وعقد النكاح.

## التلبية

* يصدح بالتلبية: "لبيك اللهم لبيك، لبيك لا شريك لك لبيك، إن الحمد والنعمة لك والملك، لا شريك لك".
* يستمر بالتلبية حتى يصل إلى البيت (الكعبة).

---

## 3. دخول المسجد الحرام

* يقدم رجله اليمنى ويقول: "بسم الله والصلاة والسلام على رسول الله، أعوذ بالله العظيم وبوجهه الكريم وسلطانه القديم من الشيطان الرجيم، اللهم افتح لي أبواب رحمتك".
* يقطع التلبية فور البدء بالطواف.

---

## 4. الطواف حول الكعبة

## شروط الطواف

* **الطهارة:** يجب أن يكون على طهارة من الحدثين الأصغر والأكبر.
* **محيط الطواف:** يجب أن يكون الطواف **من خارج "حِجر إسماعيل"**؛ فمن طاف من داخل الحِجر لم يصح طوافه لأنه جزء من الكعبة.

## كيفية الطواف

* يبدأ من محاذاة الحجر الأسود، يستقبله ويكبر "الله أكبر".
* يطوف سبعة أشواط، جاعلاً الكعبة عن يساره.
* **الرَّمَل (للرجال):** الإسراع في المشي مع تقارب الخطى في **الأشواط الثلاثة الأولى** فقط.
* **الاضطباع (للرجال):** كشف الكتف الأيمن في **جميع أشواط الطواف فقط**.

## الأذكار واستلام الأركان

* يستلم الركن اليماني بيده (دون تقبيل أو تكبير) إن تيسر، وإلا مضى ولا يشير إليه.
* بين الركن اليماني والحجر الأسود يقول: "رَبَّنَا آتِنَا فِي الدُّنْيَا حَسَنَةً وَفِي الآخِرَةِ حَسَنَةً وَقِنَا عَذَابَ النَّارِ".
* يدعو في بقية الطواف بما شاء من خيري الدنيا والآخرة.

---

## 5. صلاة الركعتين

* **تغطية الكتف:** فور الانتهاء من الشوط السابع، يعيد المحرم رداءه على كتفيه ويغطي كتفه الأيمن قبل البدء بالصلاة.
* يصلي ركعتين خلف مقام إبراهيم إن تيسر، وإلا ففي أي مكان في الحرم.
* يقرأ في الأولى (الكافرون) وفي الثانية (الإخلاص).
* يُستحب بعد الصلاة الشرب من ماء زمزم.

---

## 6. السعي بين الصفا والمروة

## البداية من الصفا

* عند القرب من الصفا يقرأ: "إِنَّ الصَّفَا وَالْمَرْوَةَ مِن شَعَآئِرِ اللّهِ".
* يرقا الصفا ويستقبل القبلة ويكبر ويقول: "لا إله إلا الله وحده لا شريك له، له الملك وله الحمد وهو على كل شيء قدير..." (3 مرات مع الدعاء بينها).

## كيفية السعي

* يمشي من الصفا إلى المروة (هذا شوط)، ومن المروة إلى الصفا (هذا شوط ثانٍ)، وهكذا حتى يكمل 7 أشواط.
* **بين العلمين الأخضرين:** يُسن للرجال فقط الركض الشديد (الإسراع)، أما النساء فيمشين كعادتهن.
* الطهارة في السعي مستحبة وليست شرطاً.

---

## 7. التحلل من العمرة

* **للرجال:** حلق الرأس بالكامل أو تقصيره (والحلق أفضل وأكثر أجراً).
* **للنساء:** تقصير قدر أنملة (حوالي 2 سم) من جميع أطراف الشعر.
* **تمت العمرة:** بهذا يحل للمعتمر كل شيء كان محرماً عليه بالإحرام.

---

## ملاحظات ختامية

* الطواف 7 أشواط كاملة تبدأ وتنتهي عند الحجر الأسود.
* السعي 7 أشواط تبدأ بالصفا وتنتهي بالمروة.
* احرص على اتباع السنة والرفق بالآخرين وتجنب المزاحمة.

---

